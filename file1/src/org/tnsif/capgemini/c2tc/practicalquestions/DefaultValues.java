package org.tnsif.capgemini.c2tc.practicalquestions;

public class DefaultValues {
	int a;
	boolean b;
	char c;

	public static void main(String[] args) {
		DefaultValues def = new DefaultValues();
		System.out.println("Default value for int: "+def.a);
		System.out.println("Default value for boolean: "+def.b);
		System.out.println("Default value for char: "+def.c);
	}

}
