package tnsif.practicalquestions;

public class Convert {

	public static void main(String[] args) {
		int i = 23;
		double b = i;
		double c = 56.78d;
		int j = (int)c;
		System.out.println("Converted from int to double: "+b);
		System.out.println("Converted from double to int: "+j);
	}

}
