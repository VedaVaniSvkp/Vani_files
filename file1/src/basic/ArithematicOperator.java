package basic;

public class ArithematicOperator {

	public static void main(String[] args) {
	    int a=32, b=4;
	    
	    //addition
	    
	    int sum = a+b;
	    System.out.println("Addition of a and b: "+sum);
	    
	    //subtraction
	    
	    int diff = a-b;
	    System.out.println("Subtraction of a and b: "+diff);
	    
	    //multiplication
	    
	    int product = a*b;
	    System.out.println("Multiplication of a and b: "+product);
	    
	    //division 
	    
	    int quotient = a/b;
	    System.out.println("Division of a and b: "+quotient);
	    
	    //modulus
	    
	    int remainder = a%b;
	    System.out.println("Modulo division of a and b: "+remainder);

	}

}
