package basic;

public class RelationalOperator {

	public static void main(String[] args) {
		int a=129, b=135;
		System.out.println("equal to condition a==b "+(a==b));
		System.out.println("Not equal to conditon a!=b "+(a!=b));
		System.out.println("Greater than condition a>b "+(a>b));
		System.out.println("Less than condition a<b "+(a<b));
		System.out.println("Greater than or equal to a>=b "+(a>=b));
		System.out.println("Less than or equal to a<=b "+(a<=b));

	}

}
