package basic;

public class AssignmentOperator {

	public static void main(String[] args) {
		int a = 10, b=10, c=10, d=10, e=10;
		
		//add and assign
		a+=5;
		System.out.println("a after addition = "+a);
		
		//sub and assign
		b-=5;
		System.out.println("b after subtraction = "+b);
		
		//multiply and assign
		c*=5;
		System.out.println("c after multiplication = "+c);
		
		//divide and assign 
		d/=2;
		System.out.println("d after division = "+d);
		
		//modulus and assign
		e%=2;
		System.out.println("e after modulo division = "+e);

	}

}
